    private void jbtt_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtt_insertActionPerformed
        // TODO add your handling code here:
        int index = jcbx_seleccionar.getSelectedIndex();
        String seleccionado = jcbx_seleccionar.getItemAt(index);

        int ele = Integer.parseInt(jtf_insert.getText());

        if (seleccionado != "Seleccionar") {
            if (seleccionado == "Insertar Normal(porFin)") {

                L.InsertarPorFin(ele);
                jTextArea1.setText(L.toString());
            } else {
                if (seleccionado == "InsertarInicio") {
                    L.InsertarInicio(ele);
                    jTextArea1.setText(L.toString());
                } else {
                    if (seleccionado == "InsertarFin") {
                        L.InsertarFin(ele);
                        jTextArea1.setText(L.toString());
                        
                    }
                }

            }
        }
        jcbx_seleccionar.setSelectedIndex(0);
        jtf_insert.setText("");
    }//GEN-LAST:event_jbtt_insertActionPerformed

