    private void jBttn_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBttn_insertActionPerformed
        // TODO add your handling code here:
        int usuario=jcbx_insertar.getSelectedIndex();
        if(jcbx_insertar.getItemAt(usuario)=="Insertar en A"){
            A.insertar(Integer.parseInt(jtf_insert.getText()));
            jta_A.setText(A.toString());
        }else{
            B.insertar(Integer.parseInt(jtf_insert.getText()));
            jta_B.setText(B.toString()); 
        }
        //Luego de insertar se limpia el campo de texto
       jtf_insert.setText("");
        
    }//GEN-LAST:event_jBttn_insertActionPerformed

    private void jbttn_elimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbttn_elimActionPerformed
        // TODO add your handling code here:
        int usuario=jcbx_eliminar.getSelectedIndex();//Devuelve la
        //posici√≥n del item seleccionado por el usuario
        if(jcbx_eliminar.getItemAt(usuario)=="Eliminar en A"){
            A.eliminar(Integer.parseInt(jtf_elim.getText()));
            jta_A.setText(A.toString());
        }else{
            B.eliminar(Integer.parseInt(jtf_elim.getText()));
            jta_B.setText(B.toString()); 
        }
        jtf_elim.setText("");
    }//GEN-LAST:event_jbttn_elimActionPerformed

    private void jbttn_calcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbttn_calcActionPerformed
        // TODO add your handling code here:
        int usuario=jcbx_res.getSelectedIndex();
        if(jcbx_res.getItemAt(usuario)=="Union"){
            C=new Conjunto();
            C.union(A, B);
            jta_res.setText(C.toString());
        }else{
            if(jcbx_res.getItemAt(usuario)=="Interseccion"){
                C=new Conjunto();
                C.interseccion(A, B);
                jta_res.setText(C.toString());
            }else{//Verificar subconjuntos
                C=new Conjunto();  
                if(C.subconjunto(A, B)&& C.subconjunto(B, A)){
                    jta_res.setText("A es subconjunto de B y B es subconjunto de A");
                }else{
                if(C.subconjunto(A, B)){
                   jta_res.setText("A es subconjunto de B"); 
                }else{
                    if(C.subconjunto(B, A)){
                     jta_res.setText("B es subconjunto de A");   
                    }else{
                     jta_res.setText("No hay subconjuntos");  
                    }
                }
                }
            }
        }
        
    }//GEN-LAST:event_jbttn_calcActionPerformed

